    private void cboEmpleadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cboEmpleadoActionPerformed
        int indexSeleccionado = cboEmpleado.getSelectedIndex();
        lblEmpleado.setText(empleados.get(indexSeleccionado).getNombre() + " "
                + empleados.get(indexSeleccionado).getApellido());
        Puesto puesto = empleados.get(indexSeleccionado).getPuesto();
        for (int i = 0; i < puestos.size(); i++) {
            if (puestos.get(i).equals(puesto)) {
                lblPuesto.setText(puestos.get(i).getNombre() + " - $ " + puestos.get(i).getSueldo());
                double sueldo = puestos.get(i).getSueldo();
                double jubilacion = sueldo * 11 / 100;
                double obraSocial = sueldo * 3 / 100;
                double sindicato = sueldo * 2 / 100;
                double totalDescuento = jubilacion + obraSocial + sindicato;
                double totalPercibir = sueldo - totalDescuento;
                lblJubilacion.setText("$ " + String.valueOf(jubilacion));
                lblObraSocial.setText("$ " + String.valueOf(obraSocial));
                lblSindicato.setText("$ " + String.valueOf(sindicato));
                lblTotalDescuentos.setText("$ " + String.valueOf(totalDescuento));
                lblTotalPercibir.setText("$ " + String.valueOf(totalPercibir));
                break;
            }
        }
    }//GEN-LAST:event_cboEmpleadoActionPerformed

