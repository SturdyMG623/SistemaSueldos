    public void leerPuestos() {
        try {
            FileInputStream archivo = new FileInputStream("puestos.dat");
            ObjectInputStream inputArchivo = new ObjectInputStream(archivo);
            boolean finDeArchivo = false;
            while (!finDeArchivo) {
                try {
                    puestos.add((Puesto) inputArchivo.readObject());
                } catch (EOFException eof) {
                    finDeArchivo = true;
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, e.getMessage(), "Error",
                            JOptionPane.ERROR_MESSAGE);
                }
            }
            inputArchivo.close();
        } catch (IOException e) {
            JOptionPane.showMessageDialog(null, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

    public void leerEmpleados() {
        try {
            FileInputStream archivo = new FileInputStream("empleados.dat");
            ObjectInputStream inputArchivo = new ObjectInputStream(archivo);

            boolean finDeArchivo = false;
            while (!finDeArchivo) {
                try {
                    empleados.add((Empleado) inputArchivo.readObject());
                } catch (EOFException eof) {
                    finDeArchivo = true;
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
                }
            }
            inputArchivo.close();
        } catch (IOException e) {
            JOptionPane.showMessageDialog(null, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
        cargarCombo();
    }

    public void cargarCombo() {
        String[] empleadoArray = new String[empleados.size()];
        for (int i = 0; i < empleados.size(); i++) {
            empleadoArray[i] = empleados.get(i).getNombre() + " " + empleados.get(i).getApellido();
        }
        cboEmpleado.setModel(new javax.swing.DefaultComboBoxModel<>(empleadoArray));
        cboEmpleado.setSelectedIndex(0);
    }

