    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if (txtNombre.getText().isEmpty() || txtSueldo.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Por favor, complete todos los datos!");
        } else {
            String nombre = txtNombre.getText().trim();
            double sueldo = Double.parseDouble(txtSueldo.getText().trim());
            Puesto puesto = new Puesto(nombre, sueldo);
            puestos.add(puesto);
            guardarPuesto();
            txtNombre.setText("");
            txtSueldo.setText("");
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

