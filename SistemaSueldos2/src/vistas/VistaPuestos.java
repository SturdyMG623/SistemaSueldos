    public void leerPuestos() {
        try {
            FileInputStream archivo = new FileInputStream("puestos.dat");
            ObjectInputStream inputArchivo = new ObjectInputStream(archivo);

            boolean finDeArchivo = false;
            while (!finDeArchivo) {
                try {
                    puestos.add((Puesto) inputArchivo.readObject());
                } catch (EOFException eof) {
                    finDeArchivo = true;
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, e.getMessage());
                }
            }
            inputArchivo.close();
        } catch (IOException e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        cargarTabla();
    }

    public void cargarTabla() {
        String matriz[][] = new String[puestos.size()][2];

        for (int i = 0; i < puestos.size(); i++) {
            matriz[i][0] = puestos.get(i).getNombre();
            matriz[i][1] = "$ " + String.valueOf(puestos.get(i).getSueldo());
        }

        tblPuesto.setModel(new javax.swing.table.DefaultTableModel(
                matriz,
                new String[]{
                    "Nombre", "Sueldo"
                }
        ));

    }

    public void guardarPuesto() {
        try {
            FileOutputStream archivo = new FileOutputStream("puestos.dat");
            ObjectOutputStream outputArchivo = new ObjectOutputStream(archivo);

            for (int i = 0; i < puestos.size(); i++) {
                outputArchivo.writeObject(puestos.get(i));
            }

            outputArchivo.close();
            JOptionPane.showMessageDialog(null, "Archivo guardado!");
        } catch (IOException e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }

        cargarTabla();
    }

