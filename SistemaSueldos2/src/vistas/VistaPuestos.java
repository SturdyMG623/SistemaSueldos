    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        if (fila >= 0) {
            int res = JOptionPane.showConfirmDialog(null,
                    "¿Realmente desea eliminar a\n"
                    + puestos.get(fila).getNombre() + "?",
                    "Eliminar datos", JOptionPane.YES_NO_OPTION);
            if (res == JOptionPane.YES_OPTION) {
                puestos.remove(fila);
                guardarPuesto();
            }
        } else {
            JOptionPane.showMessageDialog(null,
                    "No hay ningún puesto seleccionado!",
                    "Error", JOptionPane.WARNING_MESSAGE);

        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        if (txtNombre.getText().isEmpty() || txtSueldo.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null,
                    "Por favor, complete todos los datos!",
                    "Error",
                    JOptionPane.WARNING_MESSAGE);
        } else {
            if (fila >= 0) {
                String nombre = txtNombre.getText().trim();
                double sueldo = Double.parseDouble(txtSueldo.getText().trim());

                puestos.get(fila).setNombre(nombre);
                puestos.get(fila).setSueldo(sueldo);

                guardarPuesto();
            } else {
                JOptionPane.showMessageDialog(null,
                        "No hay ningún puesto seleccionado!",
                        "Error",
                        JOptionPane.WARNING_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnModificarActionPerformed

    private void tblPuestoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblPuestoMouseClicked
        fila = tblPuesto.getSelectedRow();
        if (fila >= 0) {
            txtNombre.setText(puestos.get(fila).getNombre());
            txtSueldo.setText(String.valueOf(puestos.get(fila).getSueldo()));
        }
    }//GEN-LAST:event_tblPuestoMouseClicked

